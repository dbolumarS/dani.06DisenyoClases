package _02_ejemplos;

public class Constantes {

	//Lo que jace quie algo sea constante, es la palabra final,
	//si no ponemos "static" habrá una copia de la constante 
	//por cada objeto de la clase
	final int MAXIMO = 10;
	
	//poniendolas "static" solo habrá una copia de la constante 
	//que compartirán los objetos de la clase
	final static int MINIMO = 0;	
}
